package com.ellisiumx.elrankup.machine;

public class MachineFriend {
    private int id;
    private String name;

    public MachineFriend(int id, String name) {
        this.id = id;
        this.name = name;
    }

    public int getId() {
        return id;
    }

    public String getName() {
        return name;
    }
}
